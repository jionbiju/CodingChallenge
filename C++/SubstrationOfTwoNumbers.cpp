//Substration of two number.
#include <iostream>

int main() {
    int a, b;
    std::cout << " Enter a number: ";
    std::cin >> a;
    std::cout << "Enter another number: ";
    std::cin >> b;
    int substraton;
    substraton= a-b;
    std::cout << a << "-" << b << "=" <<substraton;

    return 0;
}

